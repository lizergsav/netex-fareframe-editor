package com.sg.netex.model;

public class ButtonEvents {

	private String newItemUrl;
	
	private String modifyItemUrl;
	
	private String deleteItemUrl;
	
	private String backButtonUrl;

	private String getUrl;
	
	public String getNewItemUrl() {
		return newItemUrl;
	}

	public void setNewItemUrl(String newItemUrl) {
		this.newItemUrl = newItemUrl;
	}

	public String getModifyItemUrl() {
		return modifyItemUrl;
	}

	public void setModifyItemUrl(String modifyItemUrl) {
		this.modifyItemUrl = modifyItemUrl;
	}

	public String getDeleteItemUrl() {
		return deleteItemUrl;
	}

	public void setDeleteItemUrl(String deleteItemUrl) {
		this.deleteItemUrl = deleteItemUrl;
	}

	public String getBackButtonUrl() {
		return backButtonUrl;
	}

	public void setBackButtonUrl(String backButtonUrl) {
		this.backButtonUrl = backButtonUrl;
	}

	public String getGetUrl() {
		return getUrl;
	}

	public void setGetUrl(String getUrl) {
		this.getUrl = getUrl;
	}
	
}
